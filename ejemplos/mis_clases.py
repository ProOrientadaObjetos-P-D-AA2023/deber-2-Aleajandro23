# Crear dos clases en Python
# Usted defina el nombre y los atributos
# Puede usar las mismas clases usadas en Java en los ejemplos estudiados

# clase 01

class Persona:
    def __init__(self, nombre, edad):
        self.nombre = nombre
        self.edad = edad

    def saludar(self):
        print("Hola, mi nombre es", self.nombre)

# Crear una instancia de la clase Persona
persona1 = Persona("Ruben", 25)

# Acceder a los atributos de la instancia
print(persona1.nombre)  # salida: Ruben
print(persona1.edad)    # salida: 25

# Llamar al m√©todo de la instancia
persona1.saludar()      # salida: Hola, mi nombre es Ruben


